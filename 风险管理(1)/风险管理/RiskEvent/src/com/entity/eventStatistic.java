package com.entity;

public class eventStatistic {
	private String riskType;
	private String riskId;
	private String riskName;
	private String year;
	private Integer quarter;
	private Double money;
	private Integer eventnum;
	private Integer indepnum;
	private Float eventHun;
	private Float moneyHun;
	
	public String getRiskType() {
		return riskType;
	}
	public void setRiskType(String riskType) {
		this.riskType = riskType;
	}
	public String getRiskId() {
		return riskId;
	}
	public void setRiskId(String riskId) {
		this.riskId = riskId;
	}
	public String getRiskName() {
		return riskName;
	}
	public void setRiskName(String riskName) {
		this.riskName = riskName;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public Integer getEventnum() {
		return eventnum;
	}
	public void setEventnum(Integer eventnum) {
		this.eventnum = eventnum;
	}
	public Integer getIndepnum() {
		return indepnum;
	}
	public void setIndepnum(Integer indepnum) {
		this.indepnum = indepnum;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public Integer getQuarter() {
		return quarter;
	}
	public void setQuarter(Integer quarter) {
		this.quarter = quarter;
	}
	public Float getEventHun() {
		return eventHun;
	}
	public void setEventHun(Float eventHun) {
		this.eventHun = eventHun;
	}
	public Float getMoneyHun() {
		return moneyHun;
	}
	public void setMoneyHun(Float moneyHun) {
		this.moneyHun = moneyHun;
	}
	
	
}
